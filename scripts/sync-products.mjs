/**
 * Sync netlify/functions/_lib/products-server.js from src/config/products.js.
 *
 * Netlify Functions run in a separate Node context and can't import the
 * ESM source under /src directly. This script reads the canonical catalog
 * from src/config/products.js and emits a CommonJS mirror containing only
 * the server-relevant fields. Runs as part of `npm run build`.
 *
 * If you add a product or change pricing/PDFs in src/config/products.js,
 * just rebuild — this script regenerates the mirror automatically.
 */

import { writeFile } from 'node:fs/promises';
import { fileURLToPath, pathToFileURL } from 'node:url';
import path from 'node:path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.join(__dirname, '..');
const SOURCE = path.join(REPO_ROOT, 'src', 'config', 'products.js');
const OUTPUT = path.join(
  REPO_ROOT,
  'netlify',
  'functions',
  '_lib',
  'products-server.js'
);

// Fields the server actually needs. Everything else (bullets, faq, image,
// long descriptions) lives only in the client bundle.
const SERVER_FIELDS = [
  'slug',
  'name',
  'tagline',
  'priceCents',
  'currency',
  'recurring',
  'stripeProductName',
  'pdfBundle',
  'toolAccess',
  'status',
];

function pickServerFields(p) {
  const out = {};
  for (const key of SERVER_FIELDS) {
    if (p[key] !== undefined) out[key] = p[key];
  }
  return out;
}

async function main() {
  const mod = await import(pathToFileURL(SOURCE).href);
  const products = mod.PRODUCTS || mod.products;
  if (!Array.isArray(products)) {
    throw new Error(`Expected PRODUCTS array export from ${SOURCE}`);
  }

  const trimmed = products.map(pickServerFields);
  const json = JSON.stringify(trimmed, null, 2);

  const body = `/**
 * Server-side mirror of /src/config/products.js
 *
 * AUTO-GENERATED by scripts/sync-products.mjs. Do not edit by hand.
 * Run \`npm run sync:products\` (or \`npm run build\`, which calls it)
 * after changing src/config/products.js.
 *
 * Netlify Functions run in a Node context separate from /src and can't
 * import the ESM source directly, so this CommonJS mirror is what the
 * checkout and webhook functions read at runtime.
 */

const PRODUCTS = ${json};

function getProductBySlug(slug) {
  return PRODUCTS.find((p) => p.slug === slug && p.status === 'live');
}

function getStripeName(product) {
  return product.stripeProductName || product.name;
}

module.exports = { PRODUCTS, products: PRODUCTS, getProductBySlug, getStripeName };
`;

  await writeFile(OUTPUT, body, 'utf8');
  console.log(
    `  ✓ Synced ${trimmed.length} product${trimmed.length === 1 ? '' : 's'} → ${path.relative(REPO_ROOT, OUTPUT)}`
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
